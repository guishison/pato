using System;

namespace SimUDuck
{
    public abstract class Duck
    {
        public IFlyBehavior FlyBehavior { get; set; }
        public IQuackBehavior QuackBehavior { get; set; }

        public abstract void Display();

        public void Fly()
        {
            FlyBehavior.Fly();
        }

        public void Quack()
        {
            QuackBehavior.Quack();
        }

        public void Swim()
        {
            Console.WriteLine("¡Todos los patos flotan, incluso los no reales!");
        }
    }
}
